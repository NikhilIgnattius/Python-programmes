a = int(input("Enter your first number:"))
b = int(input("Enter your second number:"))

#Sum
print("The sum of two number is:",a+b)

#Difference
print("The difference of two number is:",a-b)

#Product
print("The product of two number is:",a*b)

#Quotient
print("The quotient of two number is:",a/b)

#Remainder
print("The remainder of two number is:",a%b)

#Quotient of two numbers without decimal point
print("The quotient of two numbers without decimal point of two number is:",a//b)

#Exponential form
print("The exponential power of two number is:",a**b)
